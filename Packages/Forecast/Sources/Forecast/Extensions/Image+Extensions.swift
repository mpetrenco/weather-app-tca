    static let forecastRainy = Image("Forecast Rainy", bundle: .module)
    
    static let iconSunny = Image("Icon Sunny", bundle: .module)
    static let iconCloudy = Image("Icon Cloudy", bundle: .module)
    static let iconRainy = Image("Icon Rainy", bundle: .module)
}
